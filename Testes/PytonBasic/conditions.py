# Laço de repetição FOR; Quantidade Fixa de elementos;
# Um loop 'for' em Python é usado para iterar sobre uma sequência como: (
# uma lista, tupla, dicionário, conjunto ou string)
# e executar um bloco de código para cada item na sequência.

lista = [1,2,3,4,5]
for numero in lista:
    print(numero)


